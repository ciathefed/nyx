kind: Kind,
literal: []const u8,
string_id: StringId,
span: Span,

pub fn init(kind: Kind, literal: []const u8, span: Span) Token {
    return Token{
        .kind = kind,
        .literal = literal,
        .string_id = StringInterner.INVALID_ID,
        .span = span,
    };
}

pub fn initWithId(kind: Kind, id: StringId, span: Span) Token {
    return Token{
        .kind = kind,
        .literal = "",
        .string_id = id,
        .span = span,
    };
}

pub fn format(
    self: Token,
    writer: *std.Io.Writer,
) std.Io.Writer.Error!void {
    try writer.print(
        ".{{ .kind = .{s}, .literal = \"{s}\", .string_id = {d}, .span = .{{ .start = {d}, .end = {d}, .filename = \"{s}\" }} }}",
        .{
            @tagName(self.kind),
            self.literal,
            self.string_id,
            self.span.start,
            self.span.start,
            self.span.filename,
        },
    );
}

